CRLF MACRO
MOV DL,0DH
MOV AH,02H
INT 21H
MOV DL,0AH
MOV AH,02H
INT 21H
ENDM

CROSS MACRO
MOV DL,'-'
MOV AH,02H
INT 21H
ENDM

DATA SEGMENT
DATA2 DB 'INPUT YEAR: $'
DATA3 DB 'PLEASE INPUT MONTH: $'
DATA4 DB 'PLEASE INPUT DAY: $'
Y DB 4 DUP(?)
M DB 2 DUP(?)
D DB 2 DUP(?)
DATA ENDS

STACK SEGMENT
STA DW 32 DUP(?)
TOP EQU LENGTH STA
STACK ENDS

public GETNUM
program SEGMENT 
	ASSUME CS:program,DS:DATA,SS:STACK


GETNUM PROC far
		LP1:MOV AH,01H
			INT 21H
			CMP AL,'9'
			JG ERR
			CMP AL,'0'
			JL ERR 
			MOV [BX],AL
			INC BX
			LOOP LP1
			MOV AX,0
			RET
		ERR:MOV AX,-1
			RET
		GETNUM ENDP
END