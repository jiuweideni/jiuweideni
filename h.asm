CRLF MACRO
MOV DL,0DH
MOV AH,02H
INT 21H
MOV DL,0AH
MOV AH,02H
INT 21H
ENDM

CROSS MACRO
MOV DL,'-'
MOV AH,02H
INT 21H
ENDM

DATA SEGMENT
DATA2 DB 'INPUT YEAR: $'
DATA3 DB 'PLEASE INPUT MONTH: $'
DATA4 DB 'PLEASE INPUT DAY: $'
Y DB 4 DUP(?)
M DB 2 DUP(?)
D DB 2 DUP(?)
DATA ENDS

STACK SEGMENT
STA DW 32 DUP(?)
TOP EQU LENGTH STA
STACK ENDS

CODE SEGMENT 
	ASSUME CS:CODE,DS:DATA,SS:STACK
	START:
		MOV AX,DATA
		MOV DS,AX
		MOV AX,STACK
		MOV SS,AX
		MOV SP,TOP
	    LEA DX,DATA2
		MOV AH,09H
		INT 21H
		MOV CX,4
		LEA BX,Y
		CALL GETNUM
		CRLF						
		LEA DX,DATA3
		MOV AH,09H
		INT 21H
		MOV CX,2
		LEA BX,M
		CALL GETNUM
		CRLF			
		LEA DX,DATA4
		MOV AH,09H
		INT 21H
		MOV CX,2
		LEA BX,D
		CALL GETNUM			
		CRLF								
		JMP DIP

DIP:
        LEA BX,Y
		MOV CX,4
		CALL DISD
        CROSS
		LEA BX,M
		MOV AX,[BX]
		CMP AX,'21'							 			 
		LEA BX,M
		MOV CX,2
		CALL DISD
		CROSS						
		LEA BX,D 
		MOV CX,2
		CALL DISD							
        mov ah,4ch
        int 21H
GETNUM PROC NEAR
		LP1:MOV AH,01H
			INT 21H
			CMP AL,'9'
			JG ERR
			CMP AL,'0'
			JL ERR 
			MOV [BX],AL
			INC BX
			LOOP LP1
			MOV AX,0
			RET
		ERR:MOV AX,-1
			RET
		GETNUM ENDP

DISD PROC NEAR
		LP2:MOV DL,[BX]
			MOV AH,02H
			INT 21H
			INC BX
			LOOP LP2
			RET
		DISD ENDP

GETM PROC NEAR
			RET
		GETM ENDP

MON PROC NEAR
    RET
MON ENDP
  
CODE ENDS
END START