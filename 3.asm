DATA4 DB 'PLEASE INPUT DAY: $'
Y DB 4 DUP(?)
M DB 2 DUP(?)
D DB 2 DUP(?)
DATA ENDS

STACK SEGMENT
STA DW 32 DUP(?)
TOP EQU LENGTH STA
STACK ENDS

public DISD
public GETM
public MON
