CRLF MACRO
MOV DL,0DH
MOV AH,02H
INT 21H
MOV DL,0AH
MOV AH,02H
INT 21H
ENDM

CROSS MACRO
MOV DL,'-'
MOV AH,02H
INT 21H
ENDM

